/// Analyzes two-phase borrows and reborrowing in a function body.
///
/// Walks the AST to detect two-phase borrow patterns and reborrow chains,
/// then augments the `PoloniusFacts` with appropriate reservation and
/// activation facts plus subset relationships.
pub struct TwoPhaseAnalyzer {
    /// Tracked two-phase borrows.
    two_phase_borrows: Vec<TwoPhaseBorrow>,
    /// Tracked reborrow relationships.
    reborrows: Vec<Reborrow>,
    /// Maps loan IDs to their active borrow info.
    active_loans: HashMap<u32, TwoPhaseBorrow>,
    /// Maps variable names to the loan ID holding a borrow of them.
    var_to_loan: HashMap<String, Vec<u32>>,
    /// Next loan ID for generating new loans.
    next_loan_id: u32,
    /// Next origin ID for generating new origins.
    next_origin_id: u32,
    /// Additional facts to merge.
    extra_facts: PoloniusFacts,
    /// Current program point.
    current_block: u32,
    /// Current statement index.
    current_stmt: u32,
}

impl TwoPhaseAnalyzer {
    /// Creates a new two-phase analyzer.
    ///
    /// `start_loan_id` and `start_origin_id` should be set to the next
    /// available IDs from the main fact generator.
    pub fn new(start_loan_id: u32, start_origin_id: u32) -> Self {
        Self {
            two_phase_borrows: Vec::new(),
            reborrows: Vec::new(),
            active_loans: HashMap::new(),
            var_to_loan: HashMap::new(),
            next_loan_id: start_loan_id,
            next_origin_id: start_origin_id,
            extra_facts: PoloniusFacts::new(),
            current_block: 0,
            current_stmt: 0,
        }
    }

    /// Returns the list of detected two-phase borrows.
    pub fn two_phase_borrows(&self) -> &[TwoPhaseBorrow] {
        &self.two_phase_borrows
    }

    /// Returns the list of detected reborrows.
    pub fn reborrows(&self) -> &[Reborrow] {
        &self.reborrows
    }

    /// Returns additional facts generated by two-phase analysis.
    pub fn extra_facts(&self) -> &PoloniusFacts {
        &self.extra_facts
    }

    /// Analyzes a method call for two-phase borrow pattern.
    ///
    /// Detects patterns like `v.push(v.len())` where the `&mut self`
    /// is reserved during argument evaluation and activated on call.
    pub fn analyze_method_call(
        &mut self,
        receiver_place: &Place,
        _method: &str,
        args: &[Expr],
        is_mutable_method: bool,
        span: Span,
    ) {
        if !is_mutable_method {
            return;
        }

        let reserve_point = self.current_point();

        // Create the reserved mutable loan.
        let loan = self.fresh_loan(receiver_place.clone(), Mutability::Mutable, span);

        let two_phase = TwoPhaseBorrow {
            loan: loan.clone(),
            phase: BorrowPhase::Reserved,
            reserved_at: reserve_point,
            activated_at: None,
        };

        self.active_loans.insert(loan.id, two_phase.clone());
        self.var_to_loan
            .entry(receiver_place.root().to_string())
            .or_default()
            .push(loan.id);
        self.two_phase_borrows.push(two_phase);

        // Check if any args borrow the same place (the two-phase pattern).
        let has_shared_arg = self.args_borrow_place(args, receiver_place);

        if has_shared_arg {
            // Loan stays reserved during argument evaluation.
            // It will be activated after arguments are evaluated.
            let activate_point = Point {
                block: self.current_block,
                statement: self.current_stmt + 1,
                kind: PointKind::Start,
            };
            self.activate_loan(loan.id, activate_point);
        } else {
            // Activate immediately.
            self.activate_loan(loan.id, reserve_point);
        }
    }

    /// Records a reborrow: `let r2 = &*r1` or `let r2 = &mut *r1`.
    pub fn record_reborrow(
        &mut self,
        new_place: &Place,
        original_place: &Place,
        mutability: Mutability,
        span: Span,
    ) {
        let point = self.current_point();
        let new_origin = self.fresh_origin();
        let new_loan = self.fresh_loan(new_place.clone(), mutability, span);

        // Find the original loan on the original place.
        let original_loan_id = self
            .var_to_loan
            .get(original_place.root())
            .and_then(|ids| ids.last().copied());

        if let Some(orig_id) = original_loan_id {
            if let Some(orig_two_phase) = self.active_loans.get(&orig_id) {
                let orig_loan = orig_two_phase.loan.clone();

                // Subset: new_origin ⊆ original_origin.
                // The new borrow's region is contained within the original's.
                let orig_origin = Origin(orig_id);
                self.extra_facts
                    .subset
                    .push((new_origin, orig_origin, point));

                self.reborrows.push(Reborrow {
                    new_loan: new_loan.clone(),
                    original_loan: orig_loan,
                    point,
                });
            }
        }

        // Issue the new loan.
        self.extra_facts
            .loan_issued_at
            .push((new_origin, new_loan.clone(), point));

        self.var_to_loan
            .entry(new_place.root().to_string())
            .or_default()
            .push(new_loan.id);
    }

    /// Analyzes a borrow expression for nested field-level tracking.
    ///
    /// `&self.field` borrows the struct at field granularity, allowing
    /// simultaneous borrows of different fields.
    pub fn analyze_field_borrow(&mut self, place: &Place, mutability: Mutability, span: Span) {
        let point = self.current_point();
        let origin = self.fresh_origin();
        let loan = self.fresh_loan(place.clone(), mutability, span);

        self.extra_facts
            .loan_issued_at
            .push((origin, loan.clone(), point));

        self.var_to_loan
            .entry(place.root().to_string())
            .or_default()
            .push(loan.id);
    }

    /// Sets the current program position.
    pub fn set_position(&mut self, block: u32, stmt: u32) {
        self.current_block = block;
        self.current_stmt = stmt;
    }

    /// Merges extra facts into the main fact set.
    pub fn merge_into(&self, facts: &mut PoloniusFacts) {
        facts
            .loan_issued_at
            .extend(self.extra_facts.loan_issued_at.iter().cloned());
        facts.origin_contains_loan_on_entry.extend(
            self.extra_facts
                .origin_contains_loan_on_entry
                .iter()
                .cloned(),
        );
        facts.subset.extend(self.extra_facts.subset.iter().copied());
        facts
            .loan_invalidated_at
            .extend(self.extra_facts.loan_invalidated_at.iter().cloned());
        facts
            .loan_killed_at
            .extend(self.extra_facts.loan_killed_at.iter().cloned());
    }
}

// ── Private helpers ────────────────────────────────────────────────────

impl TwoPhaseAnalyzer {
    fn current_point(&self) -> Point {
        Point {
            block: self.current_block,
            statement: self.current_stmt,
            kind: PointKind::Start,
        }
    }

    fn fresh_loan(&mut self, place: Place, mutability: Mutability, span: Span) -> Loan {
        let loan = Loan {
            id: self.next_loan_id,
            place,
            mutability,
            span,
        };
        self.next_loan_id += 1;
        loan
    }

    fn fresh_origin(&mut self) -> Origin {
        let o = Origin(self.next_origin_id);
        self.next_origin_id += 1;
        o
    }

    fn activate_loan(&mut self, loan_id: u32, point: Point) {
        if let Some(two_phase) = self.active_loans.get_mut(&loan_id) {
            two_phase.phase = BorrowPhase::Activated;
            two_phase.activated_at = Some(point);
        }
        // Update in the two_phase_borrows list too.
        for tp in &mut self.two_phase_borrows {
            if tp.loan.id == loan_id {
                tp.phase = BorrowPhase::Activated;
                tp.activated_at = Some(point);
            }
        }
    }

    /// Checks if any argument expressions borrow the given place.
    fn args_borrow_place(&self, args: &[Expr], place: &Place) -> bool {
        args.iter().any(|arg| expr_borrows_place(arg, place))
    }
}

/// Recursively checks if an expression borrows the given place.
fn expr_borrows_place(expr: &Expr, place: &Place) -> bool {
    match expr {
        Expr::Unary {
            op: UnaryOp::Ref | UnaryOp::RefMut,
            operand,
            ..
        } => expr_is_place(operand, place),
        Expr::MethodCall { receiver, .. } => expr_is_place(receiver, place),
        Expr::Call { args, .. } => args.iter().any(|a| expr_borrows_place(&a.value, place)),
        Expr::Binary { left, right, .. } => {
            expr_borrows_place(left, place) || expr_borrows_place(right, place)
        }
        _ => false,
    }
}

/// Checks if an expression refers to the given place.
fn expr_is_place(expr: &Expr, place: &Place) -> bool {
    match expr {
        Expr::Ident { name, .. } => name == place.root(),
        Expr::Field { object, field, .. } => {
            if let Some(proj) = place.projections.first() {
                if let super::facts::PlaceProjection::Field(f) = proj {
                    f == field && expr_is_place(object, &Place::from_var(place.root()))
                } else {
                    false
                }
            } else {
                expr_is_place(object, place)
            }
        }
        _ => false,
    }
}

impl Default for TwoPhaseAnalyzer {
    fn default() -> Self {
        Self::new(0, 0)
    }
}

