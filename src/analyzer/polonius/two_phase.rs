//! Two-phase borrowing and reborrowing support.
//!
//! Implements the two-phase borrow pattern where a mutable borrow is
//! initially in a "reserved" state (allowing shared access) and later
//! "activated" on the first mutable use.
//!
//! # Two-Phase Borrows
//!
//! Pattern: `vec.push(vec.len())` — the `&mut self` for `push` is reserved
//! while `len()` takes a shared borrow of `vec`. The mutable borrow is only
//! activated when `push` actually starts writing.
//!
//! # Reborrowing
//!
//! `let r2 = &*r1` creates a new loan with a subset relationship to `r1`'s
//! origin. The original borrow is suspended while the reborrow is active.

use std::collections::HashMap;

use crate::lexer::token::Span;
use crate::parser::ast::*;

use super::facts::{Loan, Mutability, Origin, Place, Point, PointKind, PoloniusFacts};

// ═══════════════════════════════════════════════════════════════════════
// Borrow phase tracking
// ═══════════════════════════════════════════════════════════════════════

/// The phase of a two-phase mutable borrow.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash)]
pub enum BorrowPhase {
    /// The borrow exists but hasn't been used mutably yet.
    /// Shared access to the borrowed place is still allowed.
    Reserved,
    /// The borrow has been used mutably — exclusive access is enforced.
    Activated,
}

impl std::fmt::Display for BorrowPhase {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        match self {
            BorrowPhase::Reserved => write!(f, "reserved"),
            BorrowPhase::Activated => write!(f, "activated"),
        }
    }
}

/// Information about a two-phase borrow.
#[derive(Debug, Clone, PartialEq, Eq)]
pub struct TwoPhaseBorrow {
    /// The loan that is in two-phase mode.
    pub loan: Loan,
    /// Current phase of the borrow.
    pub phase: BorrowPhase,
    /// The point where the borrow was reserved.
    pub reserved_at: Point,
    /// The point where the borrow was activated (if activated).
    pub activated_at: Option<Point>,
}

/// Information about a reborrow relationship.
#[derive(Debug, Clone, PartialEq, Eq)]
pub struct Reborrow {
    /// The new loan created by the reborrow.
    pub new_loan: Loan,
    /// The original loan being reborrowed.
    pub original_loan: Loan,
    /// The point where the reborrow occurred.
    pub point: Point,
}

