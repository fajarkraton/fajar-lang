//! Linker script generation for bare-metal targets.
//!
//! Generates GNU ld-compatible linker scripts with proper ELF sections
//! for text, data, bss, and stack layout.

use super::target::{Arch, TargetConfig};
use super::CodegenError;

/// Memory region definition for linker scripts.
#[derive(Debug, Clone)]
pub struct MemoryRegion {
    /// Region name (e.g., "FLASH", "RAM").
    pub name: String,
    /// Base address.
    pub origin: u64,
    /// Size in bytes.
    pub size: u64,
    /// Attributes: r=read, w=write, x=execute.
    pub attrs: String,
}

/// Linker script configuration.
#[derive(Debug, Clone)]
pub struct LinkerConfig {
    /// Memory regions.
    pub regions: Vec<MemoryRegion>,
    /// Stack size in bytes.
    pub stack_size: u64,
    /// Entry point symbol.
    pub entry: String,
}

impl LinkerConfig {
    /// Creates a default linker config for the given target.
    pub fn for_target(target: &TargetConfig) -> Self {
        match target.arch {
            Arch::Aarch64 => Self {
                regions: vec![
                    MemoryRegion {
                        name: "FLASH".into(),
                        origin: 0x4000_0000,
                        size: 64 * 1024 * 1024, // 64MB
                        attrs: "rx".into(),
                    },
                    MemoryRegion {
                        name: "RAM".into(),
                        origin: 0x8000_0000,
                        size: 128 * 1024 * 1024, // 128MB
                        attrs: "rwx".into(),
                    },
                ],
                stack_size: 8192,
                entry: "_start".into(),
            },
            Arch::Riscv64 => Self {
                regions: vec![
                    MemoryRegion {
                        name: "FLASH".into(),
                        origin: 0x2000_0000,
                        size: 16 * 1024 * 1024, // 16MB
                        attrs: "rx".into(),
                    },
                    MemoryRegion {
                        name: "RAM".into(),
                        origin: 0x8000_0000,
                        size: 64 * 1024 * 1024, // 64MB
                        attrs: "rwx".into(),
                    },
                ],
                stack_size: 8192,
                entry: "_start".into(),
            },
            Arch::X86_64 => Self {
                regions: vec![
                    MemoryRegion {
                        name: "FLASH".into(),
                        origin: 0x0010_0000, // 1MB
                        size: 16 * 1024 * 1024,
                        attrs: "rx".into(),
                    },
                    MemoryRegion {
                        name: "RAM".into(),
                        origin: 0x0200_0000,
                        size: 64 * 1024 * 1024,
                        attrs: "rwx".into(),
                    },
                ],
                stack_size: 16384,
                entry: "_start".into(),
            },
        }
    }

    /// Sets the stack size.
    pub fn with_stack_size(mut self, size: u64) -> Self {
        self.stack_size = size;
        self
    }

    /// Sets the entry point.
    pub fn with_entry(mut self, entry: &str) -> Self {
        self.entry = entry.to_string();
        self
    }
}

/// Generates a GNU ld-compatible linker script.
pub fn generate_linker_script(config: &LinkerConfig) -> Result<String, CodegenError> {
    if config.regions.is_empty() {
        return Err(CodegenError::AbiError(
            "linker config has no memory regions".into(),
        ));
    }

    let mut script = String::new();

    // Header
    script.push_str("/* Auto-generated by Fajar Lang compiler */\n\n");

    // Entry point
    script.push_str(&format!("ENTRY({})\n\n", config.entry));

    // Memory regions
    script.push_str("MEMORY\n{\n");
    for region in &config.regions {
        script.push_str(&format!(
            "    {} ({}) : ORIGIN = 0x{:08X}, LENGTH = 0x{:X}\n",
            region.name, region.attrs, region.origin, region.size
        ));
    }
    script.push_str("}\n\n");

    // Find FLASH and RAM regions
    let flash_name = config
        .regions
        .iter()
        .find(|r| r.attrs.contains('x'))
        .map(|r| r.name.as_str())
        .unwrap_or("FLASH");
    let ram_name = config
        .regions
        .iter()
        .find(|r| r.attrs.contains('w') && !r.attrs.contains('x'))
        .or_else(|| config.regions.iter().find(|r| r.attrs.contains('w')))
        .map(|r| r.name.as_str())
        .unwrap_or("RAM");

    // Sections
    script.push_str("SECTIONS\n{\n");

    // .text (code)
    script.push_str(&format!(
        "    .text :\n    {{\n        *(.text.start)\n        *(.text .text.*)\n    }} > {flash_name}\n\n"
    ));

    // .rodata (read-only data)
    script.push_str(&format!(
        "    .rodata :\n    {{\n        *(.rodata .rodata.*)\n    }} > {flash_name}\n\n"
    ));

    // .data (initialized data)
    script.push_str(&format!(
        "    .data :\n    {{\n        __data_start = .;\n        *(.data .data.*)\n        __data_end = .;\n    }} > {ram_name} AT > {flash_name}\n\n"
    ));

    // .bss (zero-initialized data)
    script.push_str(&format!(
        "    .bss :\n    {{\n        __bss_start = .;\n        *(.bss .bss.*)\n        *(COMMON)\n        __bss_end = .;\n    }} > {ram_name}\n\n"
    ));

    // Stack
    script.push_str(&format!(
        "    .stack (NOLOAD) :\n    {{\n        . = ALIGN(16);\n        __stack_bottom = .;\n        . += 0x{:X};\n        __stack_top = .;\n    }} > {ram_name}\n\n",
        config.stack_size
    ));

    // Provide symbols
    script.push_str("    /DISCARD/ :\n    {\n        *(.comment)\n        *(.note*)\n    }\n");

    script.push_str("}\n\n");

    // Stack pointer symbol
    script.push_str("PROVIDE(__stack_ptr = __stack_top);\n");

    Ok(script)
}

