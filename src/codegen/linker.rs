#[cfg(test)]
mod tests {
    use super::*;

    fn test_config() -> LinkerConfig {
        LinkerConfig {
            regions: vec![
                MemoryRegion {
                    name: "FLASH".into(),
                    origin: 0x0800_0000,
                    size: 512 * 1024,
                    attrs: "rx".into(),
                },
                MemoryRegion {
                    name: "RAM".into(),
                    origin: 0x2000_0000,
                    size: 128 * 1024,
                    attrs: "rw".into(),
                },
            ],
            stack_size: 4096,
            entry: "_start".into(),
        }
    }

    #[test]
    fn generates_entry_point() {
        let script = generate_linker_script(&test_config()).unwrap();
        assert!(script.contains("ENTRY(_start)"));
    }

    #[test]
    fn generates_memory_regions() {
        let script = generate_linker_script(&test_config()).unwrap();
        assert!(script.contains("FLASH (rx)"));
        assert!(script.contains("RAM (rw)"));
        assert!(script.contains("ORIGIN = 0x08000000"));
        assert!(script.contains("ORIGIN = 0x20000000"));
    }

    #[test]
    fn generates_text_section() {
        let script = generate_linker_script(&test_config()).unwrap();
        assert!(script.contains(".text :"));
        assert!(script.contains("*(.text .text.*)"));
        assert!(script.contains("> FLASH"));
    }

    #[test]
    fn generates_bss_section() {
        let script = generate_linker_script(&test_config()).unwrap();
        assert!(script.contains(".bss :"));
        assert!(script.contains("__bss_start"));
        assert!(script.contains("__bss_end"));
    }

    #[test]
    fn generates_stack_section() {
        let script = generate_linker_script(&test_config()).unwrap();
        assert!(script.contains(".stack (NOLOAD)"));
        assert!(script.contains("__stack_bottom"));
        assert!(script.contains("__stack_top"));
        assert!(script.contains("0x1000")); // 4096
    }

    #[test]
    fn generates_stack_pointer_symbol() {
        let script = generate_linker_script(&test_config()).unwrap();
        assert!(script.contains("PROVIDE(__stack_ptr = __stack_top)"));
    }

    #[test]
    fn custom_stack_size() {
        let config = test_config().with_stack_size(16384);
        let script = generate_linker_script(&config).unwrap();
        assert!(script.contains("0x4000")); // 16384
    }

    #[test]
    fn custom_entry_point() {
        let config = test_config().with_entry("main");
        let script = generate_linker_script(&config).unwrap();
        assert!(script.contains("ENTRY(main)"));
    }

    #[test]
    fn aarch64_default_config() {
        let target = TargetConfig::from_triple("aarch64-unknown-none").unwrap();
        let config = LinkerConfig::for_target(&target);
        let script = generate_linker_script(&config).unwrap();
        assert!(script.contains("FLASH"));
        assert!(script.contains("RAM"));
        assert!(script.contains("ENTRY(_start)"));
    }

    #[test]
    fn riscv64_default_config() {
        let target = TargetConfig::from_triple("riscv64gc-unknown-none-elf").unwrap();
        let config = LinkerConfig::for_target(&target);
        let script = generate_linker_script(&config).unwrap();
        assert!(script.contains("ORIGIN = 0x20000000")); // RISC-V FLASH
    }

    #[test]
    fn empty_regions_returns_error() {
        let config = LinkerConfig {
            regions: vec![],
            stack_size: 4096,
            entry: "_start".into(),
        };
        assert!(generate_linker_script(&config).is_err());
    }

    #[test]
    fn data_section_has_load_address() {
        let script = generate_linker_script(&test_config()).unwrap();
        // Data section should load from FLASH but live in RAM
        assert!(script.contains("> RAM AT > FLASH"));
    }

    #[test]
    fn x86_64_bare_metal_default_config() {
        let target = TargetConfig::from_triple("x86_64-unknown-none").unwrap();
        assert!(target.is_bare_metal);
        let config = LinkerConfig::for_target(&target);
        let script = generate_linker_script(&config).unwrap();
        assert!(script.contains("ENTRY(_start)"));
        assert!(script.contains("ORIGIN = 0x00100000")); // 1MB
        assert!(script.contains(".text :"));
        assert!(script.contains(".stack (NOLOAD)"));
    }

    #[test]
    fn for_target_generates_valid_script_all_archs() {
        for triple in &[
            "aarch64-unknown-none",
            "riscv64gc-unknown-none-elf",
            "x86_64-unknown-none",
        ] {
            let target = TargetConfig::from_triple(triple).unwrap();
            let config = LinkerConfig::for_target(&target);
            let script = generate_linker_script(&config).unwrap();
            assert!(
                script.contains("ENTRY(_start)"),
                "missing ENTRY for {triple}"
            );
            assert!(script.contains("MEMORY"), "missing MEMORY for {triple}");
            assert!(script.contains("SECTIONS"), "missing SECTIONS for {triple}");
            assert!(
                script.contains("__stack_ptr"),
                "missing stack symbol for {triple}"
            );
        }
    }
}
